    Vector<String> nacionalidade = new Vector<String>();

    private void RestaurarComboBox() {

        try {
            GafanhotoDAO objGafanhotoDTO = new GafanhotoDAO();
            ResultSet rs = objGafanhotoDTO.listarNacionalidade();

            while (rs.next()) {
                nacionalidade.addElement(rs.getString(1));
                cbxNacionalidade.addItem(rs.getString(1));
            }

        } catch (SQLException erro) {
        }
    }

    Vector<String> sexo = new Vector<String>();

    private void cbxSexo() {

        try {
            GafanhotoDAO objGafanhotoDTO = new GafanhotoDAO();
            ResultSet rs = objGafanhotoDTO.listarSexo();

            while (rs.next()) {
                sexo.addElement(rs.getString(1));
                cbxSexo.addItem(rs.getString(1));
            }

        } catch (SQLException erro) {
        }
    }

    private void CadastrarGafanhoto() {
        String nome, profissao;
        double peso, altura;

        nome = txtNome.getText();
        profissao = txtProfissao.getText();

        peso = Double.parseDouble(txtPeso.getText());
        altura = Double.parseDouble(txtAltura.getText());
        
        String sexo = cbxSexo.getSelectedItem().toString();
        String nacionalidade = cbxNacionalidade.getSelectedItem().toString();

        if (nome.equals("") || profissao.equals("") || peso <= 0 || altura <= 0
                || cbxNacionalidade.getSelectedItem() == null || cbxSexo.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else { 
            GafanhotoDTO objGafanhotoDTO = new GafanhotoDTO();
            
            objGafanhotoDTO.setNome(nome);
            objGafanhotoDTO.setProfissao(profissao);
            objGafanhotoDTO.setPeso(peso);
            objGafanhotoDTO.setAltura(altura);
            objGafanhotoDTO.setSexo(sexo);
            objGafanhotoDTO.setNacionalidade(nacionalidade);
            
            GafanhotoDAO cadastrar = new GafanhotoDAO();
            cadastrar.CadastrarGafanhoto(objGafanhotoDTO);
        }
    }
