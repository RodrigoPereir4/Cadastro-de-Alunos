
package com.mycompany.cursovideoDTO;

public class GafanhotoDTO {
    
    private int id_gafanhoto;
    
    private String nome, profissao, nacionalidade, sexo;
    
    private double peso, altura;

    public int getId_gafanhoto() {
        return id_gafanhoto;
    }

    public void setId_gafanhoto(int id_gafanhoto) {
        this.id_gafanhoto = id_gafanhoto;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getProfissao() {
        return profissao;
    }

    public void setProfissao(String profissao) {
        this.profissao = profissao;
    }

    public String getNacionalidade() {
        return nacionalidade;
    }

    public void setNacionalidade(String nacionalidade) {
        this.nacionalidade = nacionalidade;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

   

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }
    
    
    
}
